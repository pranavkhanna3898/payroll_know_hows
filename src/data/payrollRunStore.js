const STORAGE_KEY = 'payroll_run_store';

export const loadPayrollRunStore = () => {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) return JSON.parse(stored);
  } catch { /* noop */ }
  return { employees: MOCK_EMPLOYEES, payruns: [] };
};

export const savePayrollRunStore = (store) => {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(store));
  } catch { /* noop */ }
};

export const createNewPayrun = (month, year, employees) => ({
  id: `PR-${year}-${String(month).padStart(2, '0')}-${Date.now()}`,
  month,
  year,
  monthLabel: new Date(year, month - 1, 1).toLocaleString('default', { month: 'long' }) + ' ' + year,
  status: 'initiated',   // initiated | reviewed | tax_checked | confirmed | slips_generated
  initiatedAt: new Date().toISOString(),
  confirmedAt: null,
  employeeIds: employees.map(e => e.id),
  adjustments: {},        // { empId: { lopDays, overtimeHours, variablePayouts: {compId: amount}, arrearEntries } }
  taxOverrides: {},       // { empId: { tdsDeductedSoFar, monthsRemaining, investments80C, etc. } }
  publishedSlips: [],     // [ empId, ... ]
});

